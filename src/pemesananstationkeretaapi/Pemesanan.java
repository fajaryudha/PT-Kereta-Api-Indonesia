    private void selectData() {
         //To change body of generated methods, choose Tools | Templates.
         String kolom[] = {"Nama","No Id","Stasiun Awal","Stasiun Tujuan","Jenis Kereta","Tanggal","No Tempat","Waktu","Harga"} ;
         DefaultTableModel dtm = new DefaultTableModel(null, kolom);
         String SQL =  "SELECT * FROM pemesanan";
         ResultSet rs = KoneksiDB.executeQuery(SQL);
         try { 
            while (rs.next()){
                String Nama = rs.getString(1);
                String NoId = rs.getString(2);
                String Stasiun_Awal = rs.getString(3);
                String Stasiun_Tujuan = rs.getString(4);
                String JenisKereta = "";
                if ("Eko".equals(rs.getString(5))) {
                    JenisKereta = "Ekonomi";
                } else {
                    JenisKereta = "Eksekutif";
                }
                String  Tanggal = rs.getString(6);
                String  No_Tempat = rs.getString(7);
                String  Waktu = rs.getString(8);
                String  Harga = rs.getString(9);
            
                String data[] = {Nama,NoId,Stasiun_Awal,Stasiun_Tujuan,JenisKereta,Tanggal,No_Tempat,Waktu,Harga};
                dtm.addRow(data);
            }
         } catch (SQLException ex) {
             Logger.getLogger(Pemesanan.class.getName()).log(Level.SEVERE, null, ex);
         }
         jTable1.setModel(dtm);
    }
