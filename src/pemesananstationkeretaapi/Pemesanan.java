    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
         if ("".equals(Tfnama.getText())|| "".equals(Tfnoid.getText()) || "".equals(tfstasiun.getText()) ||"".equals(Tftanggal.getText()) || "".equals(Tftempatduduk.getText()) || "".equals(Tfharga.getText()) || "".equals(Waktu.getText()) || "".equals(StasiunAsal.getText()))
        {
            JOptionPane.showMessageDialog(this,"Lengkapi Data","Error", JOptionPane.WARNING_MESSAGE);
        } else {
            String JK = "";
            if(Ekonomi.isSelected()){
                JK = "Eko";
            } else {
                JK = "Eks";
            }
            String SQL = "INSERT INTO `pemesanan`(`nama`, `noid`, `stasiuntujuan`, `jeniskereta`, `tanggal`, `tempatduduk`, `harga`, `waktu`, `stasiun_asal`)" +  "VALUES('"
                    +Tfnama.getText()+"','"+Tfnoid.getText()+"','"+tfstasiun.getText()+"','"+JK+"','"
                    +Tftanggal.getText()+"','"+Tftempatduduk.getText()+"','"+Tfharga.getText()+"','"
                    +Waktu.getText()+"','"+StasiunAsal.getText()+"');";
            int status = KoneksiDB.execute(SQL);
            if (status == 1){
                JOptionPane.showMessageDialog(this,"Data berhasil ditambah","Sukses",JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this,"Data gagal ditambah","Gagal",JOptionPane.WARNING_MESSAGE);
            }
        }
    }                                      

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {                                       
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        if (baris != -1){
            String NIS = jTable1.getValueAt(baris,0).toString();
            String SQL = "DELETE FROM t_siswa WHERE NIS='"+NIS+"'";
            int status = KoneksiDB.execute(SQL);
            if (status == 1){
                JOptionPane.showMessageDialog(this,"Data berhasil Dihapus","Sukses" ,JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this,"Data gagal Dihapus","Sukses" ,JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,"Pilih Baris data Terlebih Dahulu ","Error" ,JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

